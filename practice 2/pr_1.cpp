#include<iostream>
using namespace std;

main()
{
    int a = 15, b = 20;
    
    // for sum 
    cout << "addition of " << a << " and " << b << " is "  << a + b << endl;

    // for subtraction 
    cout << "subtraction of " << a << " and " << b << " is "  << a - b << endl;

    // for Multiplication 
    cout << "Multiplication  of " << a << " and " << b << " is "  << a * b << endl;

    // for Division  
    cout << "Division of " << a << " and " << b << " is "  << a / b << endl;
    // for Modulo  
    cout << "Modulo of " << a << " and " << b << " is "  << a % b << endl << endl << endl;

    cout <<  a << " + " << b << " = "  << a + b << endl;
    cout <<  a << " - " << b << " = "  << a - b << endl;
    cout <<  a << " * " << b << " = "  << a * b << endl;
    cout <<  a << " / " << b << " = "  << a / b << endl;
    cout <<  a << " % " << b << " = "  << a % b << endl;


}
